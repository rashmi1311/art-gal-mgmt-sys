CREATE TABLE exhibition (
    eid NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY START WITH 1 INCREMENT BY 1,
    etitle VARCHAR2(30) NOT NULL,
    estart DATE NOT NULL,
    eend DATE,
    ename VARCHAR2(50),
    est VARCHAR2(10) NOT NULL,
    edt VARCHAR2(10) NOT NULL,
    evenue VARCHAR2(50) NOT NULL
);

CREATE TABLE gallery (
    gid NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY START WITH 1 INCREMENT BY 1,
    gname VARCHAR2(50) NOT NULL,
    gstreet VARCHAR(50) NOT NULL,
    gcity VARCHAR(30) NOT NULL,
    gstate VARCHAR(20) NOT NULL,
    gcon VARCHAR(20) NOT NULL,
    gurl VARCHAR(50) NOT NULL,
    gmail VARCHAR(50) NOT NULL
);


CREATE TABLE artist (
    aid NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY START WITH 1 INCREMENT BY 1,
    aname VARCHAR2(50) NOT NULL,
    aage NUMBER(3) NOT NULL,
    acon VARCHAR2(20) NOT NULL,
    apaintno NUMBER(7) NOT NULL,
    amail VARCHAR2(30) NOT NULL
);

CREATE TABLE painting (
    pid NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY START WITH 1 INCREMENT BY 1,
    ptitle VARCHAR2(50),
    part VARCHAR2(50) NOT NULL,
    pyear NUMBER(4) NOT NULL,
    pcost NUMBER(10),
    pmed VARCHAR2(30) NOT NULL,
    psize VARCHAR2(20) NOT NULL
);

CREATE TABLE customer (
    cid NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY START WITH 1 INCREMENT BY 1,
    cname VARCHAR2(50) NOT NULL,
    cstreet VARCHAR2(50) NOT NULL,
    ccity VARCHAR2(30) NOT NULL,
    cstate VARCHAR2(20) NOT NULL,
    ccon VARCHAR2(20) NOT NULL,
    cph NUMBER(12) NOT NULL,
    cmail VARCHAR2(50) NOT NULL
);
